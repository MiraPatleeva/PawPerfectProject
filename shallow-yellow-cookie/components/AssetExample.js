export default function RandomAnimalPage() {
  const [imageUrl, setImageUrl] = useState(null);

  useEffect(() => {
    retrieveImageUrl();
  }, []);

  const retrieveImageUrl = async () => {
    try {
      const storedImageUrl = await AsyncStorage.getItem('randomAnimalImageUrl');
      if (storedImageUrl) {
        setImageUrl(storedImageUrl);
      } else {
        generateRandomImageUrl();
      }
    } catch (error) {
      console.error('Error retrieving image URL:', error);
    }
  };

  const generateRandomImageUrl = () => {
    const randomIndex = Math.floor(Math.random() * animalImages.length);
    const randomImageUrl = animalImages[randomIndex];
    setImageUrl(randomImageUrl);

    AsyncStorage.setItem('randomAnimalImageUrl', randomImageUrl);
  };

  return (
    <View style={styles.container}>
      {imageUrl && (
        <Image
          source={{ uri: imageUrl }}
          style={styles.image}
        />
      )}
      <Button title="Generate Random Animal" color="#6e4a96" onPress={generateRandomImageUrl} />
    </View>
  );
}
  

